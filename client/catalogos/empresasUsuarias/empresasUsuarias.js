
import { Meteor } from 'meteor/meteor';
import { Mongo } from 'meteor/mongo';

import angular from 'angular'; 
import lodash from 'lodash'; 

import { mensajeErrorDesdeMethod_preparar } from '/client/imports/generales/mensajeDeErrorDesdeMethodPreparar';
import { userHasRole } from '/client/imports/generales/userHasRole';

import { EmpresasUsuarias } from '/imports/collections/catalogos/empresasUsuarias';
import { Companias } from '/imports/collections/catalogos/companias';
import { CompaniaSeleccionada } from '/imports/collections/catalogos/companiaSeleccionada';

angular.module("scrwebm").controller("EmpresasUsuarias_Controller", ['$scope', 
function ($scope) {

    $scope.catalogosEditar = userHasRole('catalogos') ||
                             userHasRole('catalogos_generales') ? true : false; 

    $scope.showProgress = false;

    // ui-bootstrap alerts ...
    $scope.alerts = [];

    $scope.closeAlert = function (index) {
        $scope.alerts.splice(index, 1);
    }

    $scope.helpers({
        companias: () => { return Companias.find({}); },
    })

    $scope.ciasUsuarias_ui_grid = {
        enableSorting: true,
        showColumnFooter: false,
        enableCellEdit: false,
        enableCellEditOnFocus: true,
        enableRowSelection: false,
        enableRowHeaderSelection: false,
        multiSelect: false,
        enableSelectAll: false,
        selectionRowHeaderWidth: 35,
        rowHeight: 25,

        onRegisterApi: function (gridApi) {

            // marcamos el contrato como actualizado cuando el usuario edita un valor
            gridApi.edit.on.afterCellEdit($scope, function (rowEntity, colDef, newValue, oldValue) {
                if (newValue != oldValue)
                    if (!rowEntity.docState)
                        rowEntity.docState = 2;
            });
        },
        // para reemplazar el field '$$hashKey' con nuestro propio field, que existe para cada row ...
        rowIdentity: function (row) {
            return row._id;
        },
        getRowIdentity: function (row) {
            return row._id;
        }
    }

    $scope.ciasUsuarias_ui_grid.columnDefs = [
        {
            name: 'docState',
            field: 'docState',
            displayName: '',
            cellTemplate:
                '<span ng-show="row.entity[col.field] == 1" class="fa fa-asterisk" style="color: blue; font: xx-small; padding-top: 8px; "></span>' +
                '<span ng-show="row.entity[col.field] == 2" class="fa fa-pencil" style="color: brown; font: xx-small; padding-top: 8px; "></span>' +
                '<span ng-show="row.entity[col.field] == 3" class="fa fa-trash" style="color: red; font: xx-small; padding-top: 8px; "></span>',
            enableCellEdit: false,
            enableColumnMenu: false,
            enableSorting: false,
            width: 25
        },
        {
            name: 'nombre',
            field: 'nombre',
            displayName: 'Nombre',
            width: 200,
            headerCellClass: 'ui-grid-leftCell',
            cellClass: 'ui-grid-leftCell',
            enableColumnMenu: false,
            enableCellEdit: true,
            enableSorting: true,
            type: 'string'
        },
        {
            name: 'nombreCorto',
            field: 'nombreCorto',
            displayName: 'Nombre corto',
            width: 100,
            headerCellClass: 'ui-grid-leftCell',
            cellClass: 'ui-grid-leftCell',
            enableColumnMenu: false,
            enableCellEdit: true,
            enableSorting: true,
            type: 'string'
        },
        {
            name: 'abreviatura',
            field: 'abreviatura',
            displayName: 'Abreviatura',
            width: 80,
            headerCellClass: 'ui-grid-leftCell',
            cellClass: 'ui-grid-leftCell',
            enableColumnMenu: false,
            enableCellEdit: true,
            enableSorting: true,
            type: 'string'
        },
        {
            name: 'rif',
            field: 'rif',
            displayName: 'Rif',
            width: 100,
            headerCellClass: 'ui-grid-leftCell',
            cellClass: 'ui-grid-leftCell',
            enableColumnMenu: false,
            enableCellEdit: true,
            enableSorting: true,
            type: 'string'
        },
        {
            name: 'direccion',
            field: 'direccion',
            displayName: 'Dirección',
            width: 250,
            headerCellClass: 'ui-grid-leftCell',
            cellClass: 'ui-grid-leftCell',
            enableColumnMenu: false,
            enableCellEdit: true,
            enableSorting: true,
            type: 'string'
        },
        {
            name: 'telefono',
            field: 'telefono',
            displayName: 'Teléfono',
            width: 100,
            headerCellClass: 'ui-grid-leftCell',
            cellClass: 'ui-grid-leftCell',
            enableColumnMenu: false,
            enableCellEdit: true,
            enableSorting: true,
            type: 'string'
        },
        {
            name: 'fax',
            field: 'fax',
            displayName: 'Fax',
            width: 100,
            headerCellClass: 'ui-grid-leftCell',
            cellClass: 'ui-grid-leftCell',
            enableColumnMenu: false,
            enableCellEdit: true,
            enableSorting: true,
            type: 'string'
        },
        {
            name: 'companiaNosotros',
            field: 'companiaNosotros',
            displayName: 'Compañía nosotros',
            width: 180,
            editableCellTemplate: 'ui-grid/dropdownEditor',
            editDropdownIdLabel: '_id',
            editDropdownValueLabel: 'nombre',
            editDropdownOptionsArray: lodash.chain($scope.companias).
                filter(function (c) { return (c.nosotros) ? true : false; }).
                sortBy(function (item) { return item.nombre; }).
                value(),
            cellFilter: 'mapDropdown:row.grid.appScope.companias:"_id":"nombre"',
            headerCellClass: 'ui-grid-leftCell',
            cellClass: 'ui-grid-leftCell',
            enableColumnMenu: false,
            enableCellEdit: true,
            type: 'string'
        },
        {
            name: 'delButton',
            displayName: '',
            cellTemplate: '<span ng-click="grid.appScope.deleteItem(row.entity)" class="fa fa-close redOnHover" style="padding-top: 8px; "></span>',
            enableCellEdit: false,
            enableSorting: false,
            width: 25
        }
    ]

    $scope.showProgress = true;

    // ---------------------------------------------------------
    // subscriptions ...
    let subscriptionHandle_empresasUsuaria = null;
    let subscriptionHandle_companiaSeleccionada = null; 

    subscriptionHandle_empresasUsuaria = Meteor.subscribe('empresasUsuarias', () => {
        $scope.helpers({
            empresasUsuarias: () => {
                return EmpresasUsuarias.find({}, { sort: { nombre: 1 } });
            },
        });

        $scope.ciasUsuarias_ui_grid.data = $scope.empresasUsuarias;

        subscriptionHandle_companiaSeleccionada = Meteor.subscribe('companiaSeleccionada', () => {
            $scope.helpers({
                ciaSeleccionada: () => {
                    return CompaniaSeleccionada.findOne({ userID: Meteor.userId() });
                },
            });

            $scope.showProgress = false;
            $scope.$apply();
        })
    })
    // ---------------------------------------------------------

    $scope.deleteItem = function (item) {
        item.docState = 3;
    }

    $scope.nuevo = function () {
        $scope.empresasUsuarias.push({
            _id: new Mongo.ObjectID()._str,
            docState: 1
        });
    }

    $scope.save = function () {

        $scope.showProgress = true;

        // eliminamos los items eliminados; del $scope y del collection
        var editedItems = lodash.filter($scope.empresasUsuarias, function (item) { return item.docState; });

        // nótese como validamos cada item antes de intentar guardar en el servidor

        var isValid = false;
        var errores = [];

        editedItems.forEach(function (item) {
            if (item.docState != 3) {
                isValid = EmpresasUsuarias.simpleSchema().namedContext().validate(item);

                if (!isValid) {
                    EmpresasUsuarias.simpleSchema().namedContext().validationErrors().forEach(function (error) {
                        errores.push("El valor '" + error.value + "' no es adecuado para el campo '" + error.name + "'; error de tipo '" + error.type + ".");
                    });
                }
            }
            else {
                // si el usuario intenta eliminar una compañía, rechazamos si el la cia seleccionada
                if ($scope.ciaSeleccionada && $scope.ciaSeleccionada.companiaID && $scope.ciaSeleccionada.companiaID == item._id) {
                    errores.push("La compañía <b><em>" + item.nombre + "</em></b> no puede ser eliminada, pues es la que ahora está seleccionada para el usuario.");
                }
            }
        })

        if (errores && errores.length) {
            $scope.alerts.length = 0;
            $scope.alerts.push({
                type: 'danger',
                msg: "Se han encontrado errores al intentar guardar las modificaciones efectuadas en la base de datos:<br /><br />" +
                    errores.reduce(function (previous, current) {

                        if (previous == "")
                            // first value
                            return current;
                        else
                            return previous + "<br />" + current;
                    }, "")
            });

            $scope.showProgress = false;
            return;
        }

        $scope.empresasUsuarias = [];
        $scope.ciasUsuarias_ui_grid.data = [];

        Meteor.call('empresasUsuariasSave', editedItems, (err, result) => {

            if (err) {
                const errorMessage = mensajeErrorDesdeMethod_preparar(err);

                $scope.alerts.length = 0;
                $scope.alerts.push({
                    type: 'danger',
                    msg: errorMessage
                });

                $scope.showProgress = false;
                $scope.$apply();

                return;
            }


            $scope.alerts.length = 0;
            $scope.alerts.push({
                type: 'info',
                msg: result
            });

            $scope.helpers({
                empresasUsuarias: () => {
                    // las cuenas bancarias se registran para la cia seleccionada
                    return EmpresasUsuarias.find({}, { sort: { nombre: 1 } });
                },
            });

            $scope.ciasUsuarias_ui_grid.data = $scope.empresasUsuarias;

            $scope.showProgress = false;
            $scope.$apply();
        });
    }

    $scope.$on('$destroy', function () {
        subscriptionHandle_empresasUsuaria.stop();
        subscriptionHandle_companiaSeleccionada.stop();
    })
}])